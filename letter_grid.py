def letter_grid(tur, x, y):
    tur.up()
    tur.setpos(x, y)
    tur.down()

    tur.setheading(0)
    tur.down()
    tur.fd(650)
    tur.rt(90)
    tur.fd(100)
    tur.rt(90)
    tur.fd(650)
    tur.rt(90)
    tur.fd(100)
    tur.back(50)
    tur.rt(90)
    tur.fd(650)
    tur.lt(90)
    tur.fd(50)
    for x in range(6):
        tur.lt(90)
        tur.fd(50)
        tur.lt(90)
        tur.fd(100)
        tur.rt(90)
        tur.fd(50)
        tur.rt(90)
        tur.fd(100)
